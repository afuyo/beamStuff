package com.tryg.beam.kafka.poc.model;

public class Claim {

	private String claimNumber;
	private String claimTime;
	private String claimReportTime;
	private String claimCounter;
	
	/**
	 * @return the claimNumber
	 */
	public String getClaimNumber() {
		return claimNumber;
	}
	/**
	 * @param claimNumber the claimNumber to set
	 */
	public void setClaimNumber(String claimNumber) {
		this.claimNumber = claimNumber;
	}
	/**
	 * @return the claimTime
	 */
	public String getClaimTime() {
		return claimTime;
	}
	/**
	 * @param claimTime the claimTime to set
	 */
	public void setClaimTime(String claimTime) {
		this.claimTime = claimTime;
	}
	/**
	 * @return the claimReportTime
	 */
	public String getClaimReportTime() {
		return claimReportTime;
	}
	/**
	 * @param claimReportTime the claimReportTime to set
	 */
	public void setClaimReportTime(String claimReportTime) {
		this.claimReportTime = claimReportTime;
	}
	/**
	 * @return the claimCounter
	 */
	public String getClaimCounter() {
		return claimCounter;
	}
	/**
	 * @param claimCounter the claimCounter to set
	 */
	public void setClaimCounter(String claimCounter) {
		this.claimCounter = claimCounter;
	}
	

}
